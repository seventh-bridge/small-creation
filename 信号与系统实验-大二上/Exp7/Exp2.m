clear;
clc;
fs = 5120;
n = (0: 1/fs: 0.2);
f1 = 150;
x1 = 3 * sin(2 * pi * f1 * n);
f2 = 3000;
x2 = 3 * sin(2 * pi * f2 * n);
h1 = fft(x1, size(n, 2));
subplot(2, 1, 1);
plot(n, abs(h1));
grid;
title('幅度谱');
subplot(2, 1, 2);
plot(n, angle(h1));
grid;
title('相位谱');
h2 = fft(x2, size(n, 2));
figure(2);
subplot(2, 1, 1);
plot(n, abs(h2));
grid;
title('幅度谱');
subplot(2, 1, 2);
plot(n, angle(h2));
grid;
title('相位谱');