clear;
clc;
fs = 1000;
t = (0: 1/fs: 0.2);
f0 = 50;
x = cos(2* pi * f0 * t);
subplot(2, 2, 1);
plot(t, x);
title('连续时间正弦信号');
n1 = (0: 0.01: 0.2);
x1 = cos(2* pi * f0 * n1);
subplot(2, 2, 2);
stem(n1, x1);
title('采样间隔是0.01s');
n2 = (0: 0.002: 0.2);
x2 = cos(2 * pi * f0 * n2);
subplot(2, 2, 3);
stem(n2, x2);
title('采样间隔是0.002s');
n3 = (0: 0.001: 0.2);
x3 = cos(2 * pi * f0 * n3);
subplot(2, 2, 4);
stem(n3, x3);
title('采样间隔是0.001s');